            ],
            'page_title'         => __("Recovery Arts"),
        ];
        return view('Art::frontend.vendorArt.index', $data);
    }

    public function restore($id)
    {
        $this->checkPermission('art_delete');
        $user_id = Auth::id();
        $query = $this->artClass::onlyTrashed()->where("author_id", $user_id)->where("id", $id)->first();
        if(!empty($query)){
            $query->restore();
        }
        return redirect(route('art.vendor.recovery'))->with('success', __('Restore art success!'));
    }

