    public function index(Request $request)
    {
        $this->checkPermission('business_view');
        $query = $this->business::query() ;
        $query->orderBy('id', 'desc');
        if (!empty($business_name = $request->input('s'))) {
            $query->where('title', 'LIKE', '%' . $business_name . '%');
            $query->orderBy('title', 'asc');
        }
        if (!empty($is_featured = $request->input('is_featured'))) {
            $query->where('is_featured', 1);
        }
        if (!empty($location_id = $request->query('location_id'))) {
            $query->where('location_id', $location_id);
        }
        if ($this->hasPermission('business_manage_others')) {
            if (!empty($author = $request->input('vendor_id'))) {
                $query->where('create_user', $author);
            }
        } else {
            $query->where('create_user', Auth::id());
        }
        $data = [
            'rows'               => $query->with(['author'])->paginate(20),
            'business_manage_others' => $this->hasPermission('business_manage_others'),
            'breadcrumbs'        => [
                [
                    'name' => __('Businesss'),
                    'url'  => route('business.admin.index')
                ],
                [
                    'name'  => __('All'),
                    'class' => 'active'
                ],
            ],
            'page_title'=>__("Business Management")
        ];
        return view('Business::admin.index', $data);
    }

    public function recovery(Request $request)
