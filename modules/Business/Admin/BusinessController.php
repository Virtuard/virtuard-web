    public function edit(Request $request, $id)
    {
        $this->checkPermission('business_update');
        $row = $this->business::find($id);
        if (empty($row)) {
            return redirect(route('business.admin.index'));
        }
        $translation = $row->translate($request->query('lang',get_main_lang()));
        if (!$this->hasPermission('business_manage_others')) {
            if ($row->author_id != Auth::id()) {
                return redirect(route('business.admin.index'));
            }
        }
        $data = [
            'row'            => $row,
            'translation'    => $translation,
            "selected_terms" => $row->terms->pluck('term_id'),
            'attributes'     => $this->attributes::where('service', 'business')->get(),
            'business_category'  => $this->businessCategoryClass::where('status', 'publish')->get()->toTree(),
            'business_location'  => $this->location::where('status', 'publish')->get()->toTree(),
            'location_category' => $this->locationCategoryClass::where('status', 'publish')->get(),
            'enable_multi_lang'=>true,
            'breadcrumbs'    => [
                [
                    'name' => __('Businesss'),
                    'url'  => route('business.admin.index')
                ],
                [
                    'name'  => __('Edit Business'),
                    'class' => 'active'
                ],
            ],
            'page_title'=>__("Edit: :name",['name'=>$row->title]),
        ];

        return view('Business::admin.detail', $data);
    }

