    public function storeApi(Request $request, $id = null)
    {
        if ($id > 0) {
            $this->checkPermission('hotel_update');
            $row = $this->hotelClass::find($id);
            if (empty($row)) {
                return response()->json(['message' => 'Hotel not found'], 404);
            }

            if ($row->author_id != Auth::id() && !$this->hasPermission('hotel_manage_others')) {
                return response()->json(['message' => 'Unauthorized'], 403);
            }
        } else {
            $this->checkPermission('hotel_create');
            $row = new $this->hotelClass();
            $row->status = setting_item('hotel_vendor_create_service_must_approved_by_admin', 0) ? 'pending' : 'publish';
            $row->author_id = Auth::id();
        }

        $dataKeys = [
            'title',
            'content',
            'slug',
            'video',
            'is_featured',
            'policy',
            'location_id',
            'address',
            'map_lat',
            'map_lng',
            'map_zoom',
            'star_rate',
            'price',
            'sale_price',
            'check_in_time',
            'check_out_time',
            'allow_full_day',
            'enable_extra_price',
            'extra_price',
            'min_day_before_booking',
            'min_day_stays',
            'enable_service_fee',
            'service_fee',
            'surrounding',
            'category_id',
            'room',
            'bed',
            'bathroom',
            'chain',
            'phone',
            'website',
            'ipanorama_id'
        ];

        $row->fillByAttr($dataKeys, $request->input());

        if ($request->hasFile('image_id')) {
            $file = $request->file('image_id');
            $filename = time() . '_' . $file->getClientOriginalName();
            $path = $file->storeAs('/hotel', $filename);
            $media = new \Modules\Media\Models\MediaFile();
            $media->file_name = $file->getClientOriginalName();
            $media->file_path = $path;
            $media->file_type = $file->getClientMimeType();
            $media->file_extension = $file->getClientOriginalExtension();
            $media->file_size = $file->getSize();
            $media->driver = config('filesystems.default');
            $media->save();

            $row->image_id = $media->id;
        }

        if ($request->hasFile('banner_image_id')) {
            $banner = $request->file('banner_image_id');
            $filename = time() . '_' . $banner->getClientOriginalName();
            $bannerPath = $banner->storeAs('/hotel', $filename);

            $mediaBanner = new \Modules\Media\Models\MediaFile();
            $mediaBanner->file_name = $banner->getClientOriginalName();
            $mediaBanner->file_path = $bannerPath;
            $mediaBanner->file_extension = $banner->getClientOriginalExtension();
            $mediaBanner->file_type = $banner->getMimeType();
            $mediaBanner->save();

            $row->banner_image_id = $mediaBanner->id;
        }


        $res = $row->saveOriginOrTranslation($request->input('lang'), true);

        if ($res) {
            if (!$request->input('lang') || is_default_lang($request->input('lang'))) {
                $this->saveTerms($row, $request);
                
                // Refresh model after saveOriginOrTranslation
                $row->refresh();
                
                $this->processCatalogFiles($row, $request);
            }

            do_action(Hook::AFTER_SAVING, $row, $request);

            if ($id > 0) {
                event(new UpdatedServiceEvent($row));
                return response()->json(['message' => 'Hotel updated', 'data' => $row], 200);
            } else {
                event(new CreatedServicesEvent($row));
                return response()->json(['message' => 'Hotel created', 'data' => $row], 201);
            }
        }

        return response()->json(['message' => 'Failed to save hotel'], 500);
    }

    public function saveTerms($row, $request)
    {
        if (empty($request->input('terms'))) {
            $this->hotelTermClass::where('target_id', $row->id)->delete();
        } else {
            $term_ids = $request->input('terms');
            foreach ($term_ids as $term_id) {
                $this->hotelTermClass::firstOrCreate([
                    'term_id' => $term_id,
                    'target_id' => $row->id,
                ]);
            }
            $this->hotelTermClass
                ::where('target_id', $row->id)
                ->whereNotIn('term_id', $term_ids)
                ->delete();
        }
    }

    protected function processCatalogFiles($model, $request)
    {
        if (!$request->has('catalogs')) {
            Log::info('Hotel Vendor processCatalogFiles: No catalogs in request');
            return;
        }

        $catalogs = $request->input('catalogs');
        Log::info('Hotel Vendor processCatalogFiles: Raw catalogs input', ['catalogs' => $catalogs, 'count' => is_array($catalogs) ? count($catalogs) : 0]);
        
        $processedCatalogs = [];

        foreach ($catalogs as $key => $catalog) {
            // Allow empty catalogs - just save whatever is provided

            $processedCatalog = [
                'name' => $catalog['name'] ?? '',
                'type' => $catalog['type'] ?? 'file',
                'url' => $catalog['url'] ?? ''
            ];

            // Handle file upload
            if (($catalog['type'] ?? 'file') === 'file' && $request->hasFile("catalogs.{$key}.file")) {
                $file = $request->file("catalogs.{$key}.file");
                
                Log::info("Hotel Vendor processCatalogFiles: Processing file upload for key {$key}", ['filename' => $file->getClientOriginalName()]);

                // Generate unique filename
                $filename = time() . '_' . $file->getClientOriginalName();
                $path = $file->storeAs('/hotel/catalogs', $filename, 'public');
                
                // Store file path directly to url field
                $processedCatalog['url'] = $path;
                Log::info("Hotel Vendor processCatalogFiles: File uploaded successfully", ['path' => $path]);
            } elseif (($catalog['type'] ?? 'file') === 'link') {
                // For link type, use the provided URL
                $processedCatalog['url'] = $catalog['url'] ?? '';
                Log::info("Hotel Vendor processCatalogFiles: Processing link for key {$key}", ['url' => $processedCatalog['url']]);
            } elseif (($catalog['type'] ?? 'file') === 'file') {
                // For file type without new upload, use existing URL from request (sent via hidden input)
                $processedCatalog['url'] = $catalog['url'] ?? '';
                Log::info("Hotel Vendor processCatalogFiles: Keeping existing file URL for key {$key}", ['url' => $processedCatalog['url']]);
            }

            $processedCatalogs[] = $processedCatalog;
        }

        Log::info('Hotel Vendor processCatalogFiles: Processed catalogs before save', [
            'count' => count($processedCatalogs),
            'catalogs' => $processedCatalogs,
            'model_id' => $model->id
        ]);

        // Save to main model directly as array (model has cast for catalogs)
        $model->catalogs = $processedCatalogs;
        $saved = $model->save();
        
        Log::info('Hotel Vendor processCatalogFiles: Save result', [
            'saved' => $saved,
            'model_id' => $model->id,
            'catalogs_after_save' => $model->catalogs
        ]);
        
        // Refresh to verify
        $model->refresh();
        Log::info('Hotel Vendor processCatalogFiles: After refresh', [
            'catalogs' => $model->catalogs
        ]);
    }

