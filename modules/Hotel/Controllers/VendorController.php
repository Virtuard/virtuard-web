            event(new UpdatedServiceEvent($query));
        }
        return redirect(route('hotel.vendor.recovery'))->with('success', __('Restore hotel success!'));
    }

    public function bulkEditHotel($id, Request $request)
    {
        $this->checkPermission('hotel_update');
        $action = $request->input('action');
        $user_id = Auth::id();
        $query = $this->hotelClass
            ::where('author_id', $user_id)
            ->where('id', $id)
            ->first();
        if (empty($id)) {
            return redirect()
                ->back()
                ->with('error', __('No item!'));
        }
        if (empty($action)) {
            return redirect()
                ->back()
                ->with('error', __('Please select an action!'));
        }
        if (empty($query)) {
            return redirect()
                ->back()
                ->with('error', __('Not Found'));
        }
        switch ($action) {
            case 'make-hide':
                $query->status = 'draft';
                break;
            case 'make-publish':
                $query->status = 'publish';
                if (!auth()->user()->checkUserPlan()) {
                    return redirect(route('user.plan'));
                }
                break;
            case 'make-private':
                $query->status = 'private';
                break;
        }
        $query->save();
        event(new UpdatedServiceEvent($query));

        return redirect()
            ->back()
            ->with('success', __('Update success!'));
    }

    public function bookingReportBulkEdit($booking_id, Request $request)
    {
        $status = $request->input('status');
        if (!empty(setting_item('hotel_allow_vendor_can_change_their_booking_status')) and !empty($status) and !empty($booking_id)) {
            $query = $this->bookingClass::where('id', $booking_id);
            $query->where('vendor_id', Auth::id());
            $item = $query->first();
            if (!empty($item)) {
                $item->status = $status;
                $item->save();

                if ($status == Booking::CANCELLED) {
                    $item->tryRefundToWallet();
                }

                event(new BookingUpdatedEvent($item));
                return redirect()
                    ->back()
                    ->with('success', __('Update success'));
            }
            return redirect()
                ->back()
                ->with('error', __('Booking not found!'));
        }
        return redirect()
            ->back()
            ->with('error', __('Update fail!'));
    }
