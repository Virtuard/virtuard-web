    public function editCultural(Request $request, $id)
    {
        $this->checkPermission('cultural_update');
        $user_id = Auth::id();
        $row = $this->culturalClass::where("author_id", $user_id);
        $row = $row->find($id);
        if (empty($row)) {
            return redirect(route('cultural.vendor.index'))->with('warning', __('Cultural not found!'));
        }
