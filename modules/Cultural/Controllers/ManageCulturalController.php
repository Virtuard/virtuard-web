    public function restore($id)
    {
        $this->checkPermission('cultural_delete');
        $user_id = Auth::id();
        $query = $this->culturalClass::onlyTrashed()->where("author_id", $user_id)->where("id", $id)->first();
        if(!empty($query)){
            $query->restore();
        }
        return redirect(route('cultural.vendor.recovery'))->with('success', __('Restore cultural success!'));
    }

